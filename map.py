# Sets each type of image to a value inside a list. Then uses a list of lists to build the map above.
sky_tiles = [blue_sky, cloud_top, cloud]


def draw_background(bg_size):
    bg = pygame.Surface(bg_size)
    # draw each tile in the bg

    for r, grid_list in enumerate(sky):
        for t, grid_element in enumerate(grid_list):
            # blit the correct tile on the screen
            bg.blit(sky_tiles[grid_element], (t * TILE_SIZE, r * TILE_SIZE))
    return bg
